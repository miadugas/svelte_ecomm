        The Black Mirror™was spawned in 2008 by Owner Audra. 
        The Black Mirror™ began as an art endeavor making Victorian themed anthropomorphic taxidermy dioramas and 
        jewels made with recycled animal parts, bone and teeth jewelry. Victorian inspired Taxidermy Memento Mori, as the inspiration behind The Black Mirror™
        was the Victorian Mourning Era and the memorial rituals that Victorian's practiced. 
        <br>
        Audra was also intrigued and appreciative of the use of Natural items and personal effects like hair and baby 
        teeth or beetles in their jewels. It struck Audra to create her own designs inspired from Victorian sentiment 
        to create jewels made with these unique pieces, as a Memorial to the specific piece.                                                      
